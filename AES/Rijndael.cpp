uint8_t Rijndael_G_Mult(uint8_t a, uint8_t b) {
    uint8_t c = 0;
    for (int i = 0; i < 8; i++) {
        uint8_t s = b & 1;
        s ^= s << 1;
        s ^= s << 2;
        s ^= s << 4;

        c ^= s & a;

        s = a >> 7;
        s ^= (s << 1) ^ (s << 3) ^ (s << 4);
        a <<= 1;
        a ^= s;

        b >>= 1;
    }

    return c;
}

void Rijndael_Sub_Bytes(uint8_t* state) {
    for (int i = 0; i < 16; i++) {
        state[i] = Rijndael_S_Box[state[i]];
    }
}

void Rijndael_Inverse_Sub_Bytes(uint8_t* state) {
    for (int i = 0; i < 16; i++) {
        state[i] = Rijndael_Inverse_S_Box[state[i]];
    }
}

void Rijndael_Shift_Rows(uint8_t* state) {
    uint8_t t = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = t;

    t = state[2];
    state[2] = state[10];
    state[10] = t;
    t = state[6];
    state[6] = state[14];
    state[14] = t;

    t = state[3];
    state[3] = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = t;
}


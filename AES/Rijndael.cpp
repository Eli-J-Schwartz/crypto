void Rijndael_Mix_Columns(uint8_t* state) {
    for (int i = 0; i < 4; i++) {
        uint8_t t1 = state[i*4+0];
        uint8_t t2 = state[i*4+1];
        uint8_t t3 = state[i*4+2];
        uint8_t t4 = state[i*4+3];

        state[i*4+0] = Rijndael_G_Mult(t1, 2) ^ Rijndael_G_Mult(t2, 3) ^ Rijndael_G_Mult(t3, 1) ^ Rijndael_G_Mult(t4, 1);
        state[i*4+1] = Rijndael_G_Mult(t1, 1) ^ Rijndael_G_Mult(t2, 2) ^ Rijndael_G_Mult(t3, 3) ^ Rijndael_G_Mult(t4, 1);
        state[i*4+2] = Rijndael_G_Mult(t1, 1) ^ Rijndael_G_Mult(t2, 1) ^ Rijndael_G_Mult(t3, 2) ^ Rijndael_G_Mult(t4, 3);
        state[i*4+3] = Rijndael_G_Mult(t1, 3) ^ Rijndael_G_Mult(t2, 1) ^ Rijndael_G_Mult(t3, 1) ^ Rijndael_G_Mult(t4, 2);
    }
}

void Rijndael_Inverse_Mix_Columns(uint8_t* state) {
    for (int i = 0; i < 4; i++) {
        uint8_t t1 = state[i*4+0];
        uint8_t t2 = state[i*4+1];
        uint8_t t3 = state[i*4+2];
        uint8_t t4 = state[i*4+3];

        state[i*4+0] = Rijndael_G_Mult(t1, 14) ^ Rijndael_G_Mult(t2, 11) ^ Rijndael_G_Mult(t3, 13) ^ Rijndael_G_Mult(t4, 9);
        state[i*4+1] = Rijndael_G_Mult(t1, 9) ^ Rijndael_G_Mult(t2, 14) ^ Rijndael_G_Mult(t3, 11) ^ Rijndael_G_Mult(t4, 13);
        state[i*4+2] = Rijndael_G_Mult(t1, 13) ^ Rijndael_G_Mult(t2, 9) ^ Rijndael_G_Mult(t3, 14) ^ Rijndael_G_Mult(t4, 11);
        state[i*4+3] = Rijndael_G_Mult(t1, 11) ^ Rijndael_G_Mult(t2, 13) ^ Rijndael_G_Mult(t3, 9) ^ Rijndael_G_Mult(t4, 14);
    }
}

void Rijndael_Add_Round_Key(uint8_t* state, uint8_t* key) {
    for (int i = 0; i < 16; i++) {
        state[i] ^= key[i];
    }
}

Rijndael::Rijndael(uint8_t *key) {
    round_keys = new uint8_t[240];
    for (int i = 0; i < 32; i++) round_keys[i] = key[i];
    for (int i = 32; i < 240; i += 4) {
        if ((i & 28) == 0) {
            round_keys[i+0] = round_keys[i-32] ^ Rijndael_S_Box[round_keys[i-3]] ^ (1 << ((i >> 5) - 1));
            round_keys[i+1] = round_keys[i-31] ^ Rijndael_S_Box[round_keys[i-2]];
            round_keys[i+2] = round_keys[i-30] ^ Rijndael_S_Box[round_keys[i-1]];
            round_keys[i+3] = round_keys[i-29] ^ Rijndael_S_Box[round_keys[i-4]];
        } else if ((i & 28) == 16) {
            round_keys[i+0] = round_keys[i-32] ^ Rijndael_S_Box[round_keys[i-4]];
            round_keys[i+1] = round_keys[i-31] ^ Rijndael_S_Box[round_keys[i-3]];
            round_keys[i+2] = round_keys[i-30] ^ Rijndael_S_Box[round_keys[i-2]];
            round_keys[i+3] = round_keys[i-29] ^ Rijndael_S_Box[round_keys[i-1]];
        } else {
            round_keys[i+0] = round_keys[i-32] ^ round_keys[i-4];
            round_keys[i+1] = round_keys[i-31] ^ round_keys[i-3];
            round_keys[i+2] = round_keys[i-30] ^ round_keys[i-2];
            round_keys[i+3] = round_keys[i-29] ^ round_keys[i-1];
        }
    }
}

Rijndael::~Rijndael() {
    delete[] round_keys;
}

void Rijndael::encrypt(uint8_t *pt) {
    Rijndael_Add_Round_Key(pt, round_keys);
    for (int i = 1; i < 14; i++) {
        Rijndael_Sub_Bytes(pt);
        Rijndael_Shift_Rows(pt);
        Rijndael_Mix_Columns(pt);
        Rijndael_Add_Round_Key(pt, round_keys + 16*i);
    }
    Rijndael_Sub_Bytes(pt);
    Rijndael_Shift_Rows(pt);
    Rijndael_Add_Round_Key(pt, round_keys + 224);
}

void Rijndael::decrypt(uint8_t *ct) {
    Rijndael_Add_Round_Key(ct, round_keys + 224);
    Rijndael_Inverse_Shift_Rows(ct);
    Rijndael_Inverse_Sub_Bytes(ct);
    for (int i = 13; i > 0; i--) {
        Rijndael_Add_Round_Key(ct, round_keys + 16*i);
        Rijndael_Inverse_Mix_Columns(ct);
        Rijndael_Inverse_Shift_Rows(ct);
        Rijndael_Inverse_Sub_Bytes(ct);
    }
    Rijndael_Add_Round_Key(ct, round_keys);
}


